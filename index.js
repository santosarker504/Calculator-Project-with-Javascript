var a = prompt("Enter The First Number: ");

var b = prompt("Enter The Second Number: ");

a = parseInt(a, 10);
b = parseInt(b, 10);

var result;

result = a + b;

document.write(a+" + "+b+" = "+result+"<br>");



result = a - b;

document.write(a+" - "+b+" = "+result+"<br>");



result = a * b;

document.write(a+" * "+b+" = "+result+"<br>");



result = a / b;

document.write(a+" / "+b+" = "+result+"<br>");



result = a % b;

document.write(a+" % "+b+" = "+result+"<br>");